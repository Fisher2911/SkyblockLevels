package io.github.fisher2911.skyblocklevels.world;

public class Position2D {

    private final int x;
    private final int y;

    public Position2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
